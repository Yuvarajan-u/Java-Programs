
package Arrays;


public class Array_with_While {

    public static void main(String[] args) {
        int Num[] = {1,2,3,4,5};
        //initialization
        int i=0;
        //condition
        while(i<Num.length)
        {
            System.out.println(Num[i]);
            //increment
            i++;
        }
    }
    
}
